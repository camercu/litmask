//! Seals this fixture's wrapper at build time. With `LITMASK_UNLOCK_KEY`
//! present, `emit()` selects the External tier — the tier the runtime
//! benchmarks measure (Task 1 of the bench suite).
//!
//! Also generates the stack-backed fixtures: `mask_stack!` only accepts an
//! inline literal (no `include_str!` companion), so the same fixture bytes
//! the heap benches read from disk are emitted here as literal call sites
//! into `OUT_DIR/stack_fixtures.rs`, keeping both paths on one plaintext.

use std::{env, fs, path::Path};

const SIZES: [usize; 3] = [16, 256, 4096];

fn main() {
    litmask_build::emit();
    generate_stack_fixtures();
}

fn generate_stack_fixtures() {
    let out_dir = env::var("OUT_DIR").expect("OUT_DIR set by cargo");
    let manifest = env::var("CARGO_MANIFEST_DIR").expect("CARGO_MANIFEST_DIR set by cargo");

    let mut src =
        String::from("// @generated by build.rs from fixtures/lit_*.txt — do not edit.\n");
    for n in SIZES {
        let path = format!("{manifest}/fixtures/lit_{n}.txt");
        println!("cargo:rerun-if-changed={path}");
        let content = fs::read_to_string(&path).unwrap_or_else(|e| panic!("read {path}: {e}"));
        // `N` is the plaintext byte length, so the literal must be exactly
        // `n` bytes for `MaskStr<N>` to line up with the heap fixture.
        assert_eq!(content.len(), n, "fixture lit_{n}.txt must be {n} bytes");
        // `{content:?}` emits a correctly escaped Rust string literal whose
        // decoded bytes equal the fixture, so the macro infers `N == n`.
        src.push_str(&format!(
            "pub fn masked_stack_{n}() -> ::litmask::MaskStr<{n}> \
             {{ ::litmask::mask_stack!({content:?}) }}\n"
        ));
    }
    fs::write(Path::new(&out_dir).join("stack_fixtures.rs"), src).expect("write stack_fixtures.rs");
}
