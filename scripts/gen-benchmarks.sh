#!/usr/bin/env bash
set -euo pipefail
# Assemble docs/BENCHMARKS.md from the benchmark artifacts:
#   - target/bench/runtime.log        (divan table, from `just bench`)
#   - target/bench-build/*.json       (hyperfine results, from `just bench-build`)
# The doc is GENERATED, never hand-edited; it carries a provenance header
# (date / commit / host / toolchain) so its machine-specific numbers stay
# interpretable. Run via `just bench-doc`.

root="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$root"

runtime_log="target/bench/runtime.log"
build_dir="target/bench-build"
out="docs/BENCHMARKS.md"

missing=0
[ -f "$runtime_log" ] || { echo "missing $runtime_log — run 'just bench' first" >&2; missing=1; }
for s in clean incremental; do
    for p in dev release; do
        [ -f "$build_dir/$s-$p.json" ] || {
            echo "missing $build_dir/$s-$p.json — run 'just bench-build' first" >&2
            missing=1
        }
    done
done
[ "$missing" -eq 0 ] || exit 1

# Provenance (stamped into the header so numbers are interpretable later).
gen_date="$(date -u +%Y-%m-%d)"
commit="$(git rev-parse --short HEAD)"
git diff --quiet && git diff --cached --quiet || commit="$commit (dirty)"
host="$(uname -s) $(uname -m)"
toolchain="$(rustc --version | awk '{print $1, $2}')"

# The divan table: from its header row ("decrypt …") to end of log. The
# "Timer precision" line goes to stderr, so the captured stdout starts at
# the table header.
runtime_table="$(awk '/^decrypt /{f=1} f' "$runtime_log")"

RUNTIME_TABLE="$runtime_table" \
GEN_DATE="$gen_date" COMMIT="$commit" HOST="$host" TOOLCHAIN="$toolchain" \
BUILD_DIR="$build_dir" OUT="$out" \
python3 - <<'PY'
import json, os

gen_date = os.environ["GEN_DATE"]
commit = os.environ["COMMIT"]
host = os.environ["HOST"]
toolchain = os.environ["TOOLCHAIN"]
build_dir = os.environ["BUILD_DIR"]
out = os.environ["OUT"]
runtime_table = os.environ["RUNTIME_TABLE"]


def build_table(path, per_site):
    with open(path) as f:
        data = json.load(f)
    rows = {}
    for r in data["results"]:
        p = r["parameters"]
        rows.setdefault(int(p["n"]), {})[p["mode"]] = r["mean"] * 1000.0  # ms
    # Δ/site is only meaningful for incremental builds (it isolates the
    # per-call-site overhead); for clean builds the Δ is dominated by the
    # one-time dep compile, so a per-site figure would be misleading.
    if per_site:
        header = "| N | masked (ms) | plain (ms) | Δ (ms) | Δ/site (µs) |"
        rule = "|---:|---:|---:|---:|---:|"
    else:
        header = "| N | masked (ms) | plain (ms) | Δ (ms) |"
        rule = "|---:|---:|---:|---:|"
    lines = [header, rule]
    for n in sorted(rows):
        m = rows[n].get("masked")
        pl = rows[n].get("plain")
        delta = m - pl
        if per_site:
            lines.append(f"| {n} | {m:.1f} | {pl:.1f} | {delta:.1f} | {delta / n * 1000.0:.2f} |")
        else:
            lines.append(f"| {n} | {m:.1f} | {pl:.1f} | {delta:.1f} |")
    return "\n".join(lines)


doc = f"""# litmask benchmarks

> **Generated by `scripts/gen-benchmarks.sh` — do not edit by hand.**
> Regenerate with `just bench-doc` (after `just bench` and `just bench-build`).
>
> | | |
> |---|---|
> | Date (UTC) | {gen_date} |
> | Commit | {commit} |
> | Host | {host} |
> | Toolchain | {toolchain} |
> | Cipher | chacha20-poly1305 (default) |
>
> Numbers are machine-specific — read them as orders of magnitude, not
> guarantees. The methodology lives in the benchmark sources under
> `benches/`; this file only records one run's results.

## Runtime

Per-access cost of a masked literal under the External tier.
`decrypt_masked` returns the owned `String` masking produces (AEAD open +
its heap allocation). Two baselines bracket it: `plain_baseline`
(`&'static str`, ~0 — read-only use) and `plain_owned` (`"…".to_string()`
— the allocation a caller pays anyway). `first_use_unlock` is the
one-time cost of recovering the `mask_key` (provider KDF + wrapper open);
it includes one 16-byte decrypt, so subtract the 16 B `decrypt_masked`
point to isolate the unlock.

```text
{runtime_table}
```

## Build time

`masked_N` / `plain_N` are otherwise-identical crates with N `mask!`
sites vs N plain literals. **Clean** is a full from-scratch build, so its
Δ is dominated by compiling the litmask dependency tree (the one-time
adoption cost, roughly constant across N). **Incremental** touches only
the leaf source, isolating the per-call-site `build.rs` + proc-macro
overhead — read Δ/site from the largest N, where cargo's fixed overhead
washes out.

### Clean — dev

{build_table(f"{build_dir}/clean-dev.json", per_site=False)}

### Clean — release

{build_table(f"{build_dir}/clean-release.json", per_site=False)}

### Incremental — dev

{build_table(f"{build_dir}/incremental-dev.json", per_site=True)}

### Incremental — release

{build_table(f"{build_dir}/incremental-release.json", per_site=True)}
"""

with open(out, "w") as f:
    f.write(doc)
print(f"wrote {out}")
PY
